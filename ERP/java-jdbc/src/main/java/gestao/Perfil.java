package gestao;

/**
 * Enumeração que representa os perfis de usuário no sistema.
 * Define os dois tipos de perfis disponíveis: ADMIN e PADRÃO.
 */
public enum Perfil {

    /**
     * Perfil de administrador, com acesso total às funcionalidades do sistema.
     */
    ADMIN,

    /**
     * Perfil padrão, com acesso restrito às funcionalidades básicas do sistema.
     */
    PADRÃO
}
